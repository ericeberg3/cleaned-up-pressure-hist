function C=coord(varargin)
%COORD    Returns coordinates for GPS stations
%   C=coord(STAT,FORMAT) returns coordinates for the stations
%   listed in STAT, which can be a char array or cell array of 
%   strings.  C is a 3xn (n=length(STAT)) matrix of station coordinates
%   in the format specified by FORMAT.  Available formats are:
%
%      'xyz'        Global Cartesian coordinates (meters)
%      'llh'        Longitude, latitude, and heigth (decimal degrees, meters)
%      'text'       Nicely formatted text (meters)
%      'local'      Local horizontal coordinates (kilometers)
%      'sta_pos'    Gipsy sta_pos format
%
%   C=coord(STAT,'local',ORIGIN) returns a 2xn matrix of station coordinates
%   in the local frame of the specified ORIGIN ([lon;lat] decimal degrees).
%
%   C=coord(STAT,'sta_pos',START,DURATION) returns an nx1 cell array of strings
%   containing sta_pos entries, beginning at the time specified by START and
%   lasting for DURATION days.  START and DURATION can be omitted, in which
%   case they default to: [1990 1 1 0 0 0] and 9999999
%
%   Calling COORD without input arguments returns a list of the available stations,
%   the coordinates of which are stored in the text of this mfile.
%
%   COORD returns NaNs for station listed in STAT that are not found in the database.

%-------------------------------------------------------------------------------
%   Record of revisions:
%
%   Date          Programmer            Description of Change
%   ====          ==========            =====================
%
%   Nov20, 2018   Paul Segall           Added station 92YN
%   May 2, 2018   Kyle Anderson         Added station PUHI
%   May 7, 2017   Kyle Anderson         Renamed from coord.m to hvogpscoords.m
%   Aug 18, 2001  Peter Cervelli        Standardized code
%   Unknown       Peter Cervelli		Original Code
%
%-------------------------------------------------------------------------------

%Parse input arguments and read data from database (contained at end of this file)


data=getdata;

    if nargin < 1
        C=data(:,1);
        return
    end

    stat=upper(cellstr(varargin{1}));

%Find matching stations

    for i=length(stat):-1:1
        I=strmatch(stat{i},data(:,1),'exact');
        if isempty(I)
            C(:,i)=[NaN;NaN;NaN];
            warning(['Missing Coordinates for: ',stat{i}])
        else
            C(:,i)=data{I,2}';
        end
    end

%Format output if necessary

    if nargin > 1

        switch lower(varargin{2})
	
            case {'llh','llhd'}
                C=xyz2llh(C);

            case 'local'
                if nargin < 3
                    error('You must specify an origin to return local coordinates.')
                end
                C=llh2local(xyz2llh(C),varargin{3});

	
			case 'sta_pos'
			    if nargin < 3
	    		    t=[1990 1 1 0 0 0];
        			dur=9999999;
    			else
    	    		t=varargin{3};
        			dur=varargin{4};
                end
				for i=1:length(stat)
	    			X{i}=sprintf('%5s%5d %02d %02d %02d:%02d:%05.2f %10.2f %15.4f%15.4f%15.4f%s\n', ...
   				         stat{i},t(1:6),dur,C(:,i),'  0.00000000e+00 0.00000000e+00 0.00000000e+00');
				end
                C=X;

            case 'text'
                fprintf('%14.4f%14.4f%14.4f\n',C)

            case 'wpt'
                C=xyz2llh(C);
                for i=1:length(stat)
                    fprintf('Waypoint,D,%s,%10.6f,%10.6f,%s,%s\n',stat{i},C(2,i),C(1,i),datestr(now,'mm/dd/yyyy'),datestr(now,'HH:MM:SS'))
                end
		
        end
	end

